package kr.co.smenglish.vitalinkmgr;

public class VitaLink {
	private int rawid;
	private int academyRawid;
	
	private String academyid = "";
	private String name = "";
	private String userid = "";
	private String passwd = "";
	private String enname = "";
	private String telnum = "";
	private String shelnum = "";
	private String parentnum = "";
	private String email = "";
	
	private String tutorid = "";
	private String className = "";
	private String gradeCd = "";
	
	public int getRawid() {
		return rawid;
	}
	public void setRawid(int rawid) {
		this.rawid = rawid;
	}
	public int getAcademyRawid() {
		return academyRawid;
	}
	public void setAcademyRawid(int academyRawid) {
		this.academyRawid = academyRawid;
	}
	public String getAcademyid() {
		return academyid;
	}
	public void setAcademyid(String academyid) {
		this.academyid = academyid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUserid() {
		return userid;
	}
	public void setUserid(String userid) {
		this.userid = userid;
	}
	public String getPasswd() {
		return passwd;
	}
	public void setPasswd(String passwd) {
		this.passwd = passwd;
	}
	public String getEnname() {
		return enname;
	}
	public void setEnname(String enname) {
		this.enname = enname;
	}
	public String getTelnum() {
		return telnum;
	}
	public void setTelnum(String telnum) {
		this.telnum = telnum;
	}
	public String getShelnum() {
		return shelnum;
	}
	public void setShelnum(String shelnum) {
		this.shelnum = shelnum;
	}
	public String getParentnum() {
		return parentnum;
	}
	public void setParentnum(String parentnum) {
		this.parentnum = parentnum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTutorid() {
		return tutorid;
	}
	public void setTutorid(String tutorid) {
		this.tutorid = tutorid;
	}
	public String getClassName() {
		return className;
	}
	public void setClassName(String className) {
		this.className = className;
	}
	public String getGradeCd() {
		return gradeCd;
	}
	public void setGradeCd(String gradeCd) {
		this.gradeCd = gradeCd;
	}
}
